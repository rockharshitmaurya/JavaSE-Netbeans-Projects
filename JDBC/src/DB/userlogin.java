    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try
    {
        DBConnection db=new DBConnection();
      String uname=t1.getText();
      String upass=t2.getText();
      String type=t3.getSelectedItem().toString();
      
      db.pstmt=db.con.prepareStatement("select utype from login_page where user=? and password=?");
    db.pstmt.setString(1,uname);
    db.pstmt.setString(2, upass);
    db.rst=db.pstmt.executeQuery();
    if(db.rst.next())
    {
        System.out.println("Userdata Correct");
    if(type.equals(db.rst.getString(1) ) && type.equals("Admin"))
    {
        System.out.println("Access Granted");
        new Admin().setVisible(true);
        this.setVisible(false);
    }
    else if(type.equals(db.rst.getString(1)) && type.equals("Member"))
    {
      new member().setVisible(true);
      this.setVisible(false);
    }
    else
    {
        JOptionPane.showMessageDialog(this, "Invalid User Type");
    }
    }
    else
        JOptionPane.showMessageDialog(this, "Invalided User or Password");
    }
    catch(Exception e)
    {
        e.printStackTrace();
    }
    
    }//GEN-LAST:event_jButton1ActionPerformed

